# This program calculates how old you will be in the year 2050

# Step 1: Ask the user for their current age
current_age = int(input("How old are you? "))

# Step 2: Calculate future age (from 2023 to 2050 is 27 years)
future_age = current_age + 27

# Step 3: Print the result
print("In 2050, you will be", future_age, "years old.")